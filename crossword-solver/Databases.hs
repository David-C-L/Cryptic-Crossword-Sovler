--
-- Some useful text expansion rules...
--

-- Expand text in all possible ways, e.g.
--   by-law -> bylaw
--   circles -> oo, os
--   can't -> cannot
expandText s
  = collapseHyphens s ++
    expandAbbreviations s ++
    expandApostrophes s 

collapseHyphens s
  | not (elem ' ' s) && elem '-' s = [filter (/='-') s]
  | otherwise = []
    
--
-- circles -> oo, os etc.
-- circle's -> o, os, ois
-- circle -> []
--
expandAbbreviations s
  | isPlural s    = concat [[a ++ a, a ++ "s"] |
                             a <- abbreviations (makeSingular s)]
  | post == "\'s" = concat [[a, a ++ "s"] |
                             a <- abbreviations pre]
  | otherwise  = []
  where
    (pre, post) = break (== '\'') s

--
-- I'm -> i/iam/im, I've -> ive/ihave.
-- Dog's breakfast -> [] (two words).
--
expandApostrophes w
  | elem ' ' w = []
  | null post  = []
  | otherwise  = expand (tail post)
  where
    (pre, post) = break (== '\'') w
    expand cont
      | s == "im"    = ["i", "im", "iam"]
      | s == "wont"  = ["wont", "willnot"]
      | s == "cant"  = ["cant", "cannot"]
      | s == "isnt"  = ["isnt", "isnot"]
      | s == "arent" = ["arent", "arenot"]
      | otherwise    = fromMaybe [] (lookup cont table)
      where
        s = pre ++ cont
    table = [("s",  add pre ["", "s", "is"]),
             ("t",  add pre ["t", "ot"]),
             ("ve", add pre ["ve", "have"]),
             ("ll", add pre ["ll", "will", "shall"])]
    add pre s = map (pre ++) s

