-- Special cases for single words comprising an embedded indicator
-- and target string. E.g.
--   gateshead -> g
--   bucktail -> k
--   headwaters -> w
--   backwater -> r
--   last-gasp -> p
--   middle-aged -> ge
--
-- However, we don't want heads -> s etc.
--
specialTextCases :: String -> [String]
specialTextCases s
  | elem ' ' s = []
  | otherwise  = concat [fh, bh, ft, bt, m]
  where
    n = length s
    fh = map head' (concatMap extractHeadString heads)
    bh = map head' (concatMap extractTailString heads)
    ft = map last' (concatMap extractHeadString tails)
    bt = map last' (concatMap extractTailString tails)
    m  = concatMap mid' (concatMap extractHeadString mids)
    head' ""  = ""
    head' [_] = ""
    head' s   = [head s]
    last' ""  = ""
    last' [_] = ""
    last' s   = [last s]
    mid' s    
      | len > 2 
        = [take (len - 2 * k) (drop k s) | k <- [1 .. len `div` 2]]
      | otherwise 
        = []
       where
         len = length s
    heads = ["head", "first", "front", "leader", "mouth", "top"]
    tails = ["tail", "last", "end", "back", "bottom"]
    mids  = ["middle", "centre"]
    extractHeadString :: String -> [String]
    extractHeadString w
      | n > k && take k s == w = [skipHyphen $ drop k s]
      | otherwise              = []
      where
        k = length w
    extractTailString :: String -> [String]
    extractTailString w
      | n > k && drop (n - k) s == w = [skipHyphen' $ take (n - k) s]
      | otherwise                    = []
      where
        k = length w

    skipHyphen' s
      = reverse (skipHyphen (reverse s))

    skipHyphen ('-' : s) 
      = s
    skipHyphen s 
      = s

