isSynonym w w'
  = elem w (synonyms w')

checkSyn s s'
  = elem s $ synonyms s'

hasSynonym 
  = (not . null . synonyms)

onlyHasCompoundSynonym s
  = null (synonyms' s) && not (null (compoundSynonyms s))


