------- ABBREVIATONS --------

hasAbbreviation s
  = Map.member s abbreviationsTable

abbreviations s
  = mlookUp s abbreviationsTable

invertedAbbreviationsTable 
  = Map.fromList [(a, strs) | a <- nub (concatMap snd allAbbreviations),
                              let strs = [s | (s, as) <- allAbbreviations, 
                                           elem a as]]

------ HOMOPHONES ------

--
-- A few additions are useful, e.g. thai's -> thais -> ties
-- Note that the homophone table only maps single words,
-- so multiple words are mapped individually and then 
-- concatenated all ways, e.g. "you see" -> "uc", "youc", etc.
--
homophones s
  = mlookUp s homophoneTable
      
homophoneTable 
  = Map.fromList (Prelude.concatMap makeMap homophonesTable)
  where
    makeMap xs = [(k, xs Data.List.\\ [k]) | k <- xs]

