-- Synonyms 
-- 
-- All text has itself as a synonym, even if it not recognised
-- as a word, e.g. "xyz".
-- All synonyms comprise a single word. Hyphens are retained
-- and the unhyphenated text is also included.
-- Note: expandPossessiveNoun calls synonyms'.
-- To go back to 'vanilla' synonyms, remove the bit after ++
-- Note: We could remove pointless hypernyms, e.g. entity, which 
-- comes back from many searches. The function to do it needs work.
--
synonyms :: String -> [String]
synonyms s
  = syns
  where
    syns = synonyms' s ++
           expandPossessiveNoun s

--
-- The compoundSynonyms function is a cheap attempt to spot synonyms
-- of multi-word phrases, e.g. place of retail -> department.
-- Hyphens are replaced with spaces, for example 
-- fully-grown -> mature.
-- We arbitrarily limit the number of non-stop words (here to two),
-- although three-word compounds are also admitted if two of
-- them come from a hyphenated word, e.g. part-shiny metal, which
-- could resolve to any metal synonym, for example.
-- Compound synonyms add to the number of *valid* parse trees, 
-- as fewer can be pruned. 
-- E.g. "place of retail" as a definition has no
-- synonyms in the thesaurus, yet the union of the individual
-- synonyms vary in length from 1 to 42.
--
compoundSynonyms s
  | n <= 2    = nub (concatMap synonyms' ws)
  | otherwise = []
  where
    ws = activeDefWords s
    n = length ws

activeDefWords s
  = words (deHyphenate s) \\ stopWords

deHyphenate = map (\c -> if c == '-' then ' ' else c)

--
-- Here we only use SINGLE-word synonyms from the thesaurus. This
-- may need to be changed in future.
-- Note: Some manual synonyms may have a prefix that's also in the
-- in the qualifiers, e.g. former partner. In that case we want
-- the synonyms of former partner and also partner.
--
synonyms' s
  = databaseSyns ++ indirectSyns
  where
    (hasNoise, s')  = removePrefixWords prefixNoiseWords s
    syns1 = if hasNoise then dbSyns s' else []
    (hasQualifier, s'') = removePrefixWords qualifiers s'
    syns2 = if hasQualifier then dbSyns s'' else []
    databaseSyns = dbSyns s ++ syns1 ++ syns2
    indirectSyns = maybe [] dbSyns (lookup s'' indirectSynonymsTable)
    dbSyns s
      = map (filter (/= ' ')) syns
      where
        syns
          = textExpansions s ++ others ++
            if null others && elem '-' s
            then otherExpansions (deHyphenate s)
            else []
        others = otherExpansions s
        otherExpansions s
          = mlookUp s manualSynsTable ++
            lookUp s nameSynonyms ++
            foreignPhrases s ++
            fromMaybe (thesaurusSynonyms s) 
                      (lookup s specialSynonymCases)

--
-- cat's -> cat, tiger, ...
--
expandPossessiveNoun s
  | post == "\'s" = synonyms' pre
  | otherwise     = []
  where
    (pre, post) = break (== '\'') s

--
-- NOT CURRENTLY USED & NEEDS WORK
-- The hypernyms "object", "human action", "entity", "abstract entity" etc.
-- appear in the synonym list of many nouns and verbs forms
-- and need to be removed, otherwise
-- they lead to stupid solutions. If we have a genuine synonym
-- of either, then we put both back, as they are (approximately)
-- mutual synonyms.
-- Words like 'thing' may appear many times as synonyms 
-- of special cases, so we nub duplicates.
--
filterHypernyms s syns
  = addBack (filter (flip elem specialCases) syns')
  where
    syns' = filter (not . flip elem specialCases) syns
    specialCases = map fst specialSynonymCases
    addBack specials 
      = nub (concatMap check specials) ++ syns'
    check sp = if elem s (lookUp sp specialSynonymCases)
               then [s] 
               else []

removePrefixWords ws s
  | null rest  = (False, s)
  | elem w ws  = (True, tail rest)
  | otherwise  = (False, s) 
  where
    (w, rest) = break (== ' ') s

--
-- This gives the raw synonyms from the thesaurus (no bells or whistles)
--
thesaurusSynonyms s
    = map (map toLower) 
          (filter (not . elem ' ') (filter (not . null) (thesaurusLookup s)))

--
-- Multi-word text will be dealt with by charades of
-- single-words text, so we don't concatenate such cases, 
-- e.g. "rich people" should not have a synonym "richpeople".
-- However, hyphenated text should have the unhyphenated
-- text as a synonym, e.g. boss-head -> bosshead, b, ...
-- The specialTextCases catches embebbed take-outs.
--
textExpansions :: String -> [String]
textExpansions s
  = abbreviations s ++ nub syns
  where
    syns
      | elem ' ' s = []
      | otherwise  = s' ++ expandText s ++ specialTextCases s
    s' = if elem '-' s
         then []
         else [filter (not . flip elem "\'") s] 

thesaurusLookup w
  = maybe [] (\s -> map (\n -> keys ! n) s) (Map.lookup w thesaurus)

--
-- Manual synonyms, some being common occurrences and others added
-- for testing. Others augment the existind corpus, e.g. philosophers, 
-- where bacon and plato are not in the thesaurus.
-- Note: by using ++ the map may contain lists with duplicates.
--
manualSynsTable
  = foldl' addSyn Map.empty manualSynonyms
  where
    addSyn acc (s, s') = Map.insertWith (++) s [s'] acc

manualSynonyms
  = concat [[(w, w'), (w', w)] | (w, ws) <- synList, w' <- ws]

